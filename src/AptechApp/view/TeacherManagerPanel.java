    private void loadDataToTable(){
        try{
            TeacherDAO tcDAO = new TeacherDAO();
            listTC = tcDAO.getAllTC();
            DefaultTableModel dtmTC = (DefaultTableModel) tblTeacher.getModel();
            dtmTC.setRowCount(0);
            for(Teacher tc : listTC){
                Vector<Object> vtTC = new Vector<Object>();
                vtTC.add(tc.getIdTeacher());
                vtTC.add(tc.getTeacherName());
                vtTC.add(tc.getEmail());
                vtTC.add(tc.getTeachingSubject());
                vtTC.add(tc.getBirthday());
                vtTC.add(tc.getAddress());
                vtTC.add(tc.getSex());
                vtTC.add(tc.getTel());
                vtTC.add(tc.getRole());
                
                dtmTC.addRow(vtTC);
            }
            dtmTC.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
