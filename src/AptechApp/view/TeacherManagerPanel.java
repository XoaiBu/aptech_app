    private void tblTeacherMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTeacherMouseClicked
        try{
            int row = tblTeacher.getSelectedRow();
            if(row>=0){
                String id = (String) tblTeacher.getValueAt(row, 0);
                TeacherDAO tcDAO = new TeacherDAO();
                Teacher tc = tcDAO.findTCById(id);
                if(tc!=null){
                    txtIdTeacher.setText(tc.getIdTeacher());
                    txtTeacherName.setText(tc.getTeacherName());
                    txtEmail.setText(tc.getEmail());
                    cbSubject.getModel().setSelectedItem(tc.getTeachingSubject());
                    cbRole.getModel().setSelectedItem(tc.getRole());
                    txtBirthday.setText(tc.getBirthday());
                    txtAddress.setText(tc.getAddress());
                    rbMale.setSelected(tc.getSex()==1?true:false);
                    txtTel.setText(tc.getTel());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblTeacherMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(txtIdTeacher.getText().equals("")){
            txtIdTeacher.requestFocus();
            return;
        }
        SubjectDAO sDAO = new SubjectDAO();
        RoleDAO rDAO = new RoleDAO();
        try{
            Teacher tc = new Teacher();
            tc.setIdTeacher(txtIdTeacher.getText());
            tc.setTeacherName(txtTeacherName.getText());
            tc.setTel(txtTel.getText());
            tc.setBirthday(txtBirthday.getText());
            tc.setAddress(txtAddress.getText());
            String idSubject = sDAO.getIdSubjectBySubjectName(String.valueOf(cbSubject.getSelectedItem()));
            tc.setTeachingSubject(String.valueOf(idSubject));
            int idRole = rDAO.getIdRoleByRole(String.valueOf(cbRole.getSelectedItem()));
            tc.setRole(idRole);
            tc.setSex(rbMale.isSelected()?1:0);
            TeacherDAO tcDAO = new TeacherDAO();
            if(tcDAO.insertTC(tc)){
                JOptionPane.showMessageDialog(tblTeacher, "Thêm thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblTeacher, "Thêm thất bại");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            TeacherDAO tcDAO = new TeacherDAO();
            SubjectDAO sDAO = new SubjectDAO();
            RoleDAO rDAO = new RoleDAO();
            Teacher tc = new Teacher();
            tc.setIdTeacher(txtIdTeacher.getText());
            tc.setTeacherName(txtTeacherName.getText());
            tc.setTel(txtTel.getText());
            tc.setBirthday(txtBirthday.getText());
            tc.setAddress(txtAddress.getText());
            tc.setEmail(txtEmail.getText());
            String idSubject = sDAO.getIdSubjectBySubjectName(String.valueOf(cbSubject.getSelectedItem()));
            tc.setTeachingSubject(String.valueOf(idSubject));
            int idRole = rDAO.getIdRoleByRole(String.valueOf(cbRole.getSelectedItem()));
            tc.setTeachingSubject(String.valueOf(idRole));
            tc.setSex(rbMale.isSelected()?1:0);
            if(tcDAO.deleteTC(txtIdTeacher.getText())){
                JOptionPane.showMessageDialog(tblTeacher, "Cập nhật thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblTeacher, "Cập nhật thất bại");
            
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

