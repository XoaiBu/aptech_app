    private void loadDataToTable(){
        try{
            SubjectDAO sjDAO = new SubjectDAO();
            listSubject = sjDAO.getAllSubject();
            DefaultTableModel dtmSj = (DefaultTableModel) tblSubject.getModel();
            dtmSj.setRowCount(0);
            for(Subject sj : listSubject){
                Vector<Object> vtSubject = new Vector<Object>();
                vtSubject.add(sj.getIdSubject());
                vtSubject.add(sj.getSubjectName());
                vtSubject.add(sj.getIdDepartment());
                dtmSj.addRow(vtSubject);
            }
            dtmSj.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void loadDataToCBDepartment(){
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            ArrayList<String> listDpm = dpmDAO.getDepartmentName();
            String[] array = listDpm.toArray(new String[listDpm.size()]);
            cbDepartment.setModel(new DefaultComboBoxModel<String>(array));
        }catch(Exception e){
            e.printStackTrace();
        }
    }
