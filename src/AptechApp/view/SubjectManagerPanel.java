    private void tblSubjectMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSubjectMouseClicked
        try{
            int row = tblSubject.getSelectedRow();
            if(row>=0){
                String id = (String) tblSubject.getValueAt(row, 0);
                SubjectDAO sjDAO = new SubjectDAO();
                Subject sj = sjDAO.findSubjectById(id);
                if(sj!=null){
                    txtIdSubject.setText(sj.getIdSubject());
                    txtSubjectName.setText(sj.getSubjectName());
                    cbDepartment.setSelectedItem(sj.getIdDepartment());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblSubjectMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            Subject sj = new Subject();
            sj.setIdSubject(txtIdSubject.getText());
            sj.setSubjectName(txtSubjectName.getText());
            String departmentId = dpmDAO.getIdDepartmentByDepartmentName(String.valueOf(cbDepartment.getSelectedItem()));
            sj.setIdDepartment(String.valueOf(departmentId));
            SubjectDAO sjDAO = new SubjectDAO();
            if(sjDAO.insertSubject(sj)){
                JOptionPane.showMessageDialog(tblSubject, "Thêm thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblSubject, "Thêm thất bại");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            Subject sj = new Subject();
            sj.setIdSubject(txtIdSubject.getText());
            sj.setSubjectName(txtSubjectName.getText());
            String departmentId = dpmDAO.getIdDepartmentByDepartmentName(String.valueOf(cbDepartment.getSelectedItem()));
            sj.setIdDepartment(String.valueOf(departmentId));
            SubjectDAO gradeDAO = new SubjectDAO();
            if(gradeDAO.updateSubject(sj)){
                JOptionPane.showMessageDialog(tblSubject, "Cập nhật thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblSubject, "Cập nhật thất bại");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

