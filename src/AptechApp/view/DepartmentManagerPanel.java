    private void loadDataToTable(){
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            listDpm = dpmDAO.getAllDepartment();
            DefaultTableModel tbmDpm = (DefaultTableModel) tblDepartment.getModel();
            tbmDpm.setRowCount(0);
            for(Department dpm : listDpm){
                Vector<Object> vtDpm = new Vector<Object>();
                vtDpm.add(dpm.getIdDeparment());
                vtDpm.add(dpm.getDepartmentName());
                tbmDpm.addRow(vtDpm);
            }
            
            tbmDpm.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
