    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            Department dpm = new Department();
            dpm.setIdDeparment(txtIdDepartment.getText());
            dpm.setDepartmentName(txtDepartmentName.getText());
            DepartmentDAO dpmDAO = new DepartmentDAO();
            if(dpmDAO.insertDepartment(dpm)){
                JOptionPane.showMessageDialog(tblDepartment, "Thêm thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblDepartment, "Thêm thất bại");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

