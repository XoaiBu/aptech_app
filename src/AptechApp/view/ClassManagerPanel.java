    private void tblClassMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClassMouseClicked
        try{
            int row = tblClass.getSelectedRow();
            if(row>=0){
                String id = (String) tblClass.getValueAt(row, 0);
                GradeDAO gradeDAO = new GradeDAO();
                Grade grade = gradeDAO.findClassById(id);
                if(grade!=null){
                    txtIdClass.setText(grade.getIdClass());
                    txtClassName.setText(grade.getClassName());
                    cbDepartment.setSelectedItem(grade.getIdDepartment());
                }
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblClassMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            Grade grade = new Grade();
            grade.setIdClass(txtIdClass.getText());
            grade.setClassName(txtClassName.getText());
            String departmentId = dpmDAO.getIdDepartmentByDepartmentName(String.valueOf(cbDepartment.getSelectedItem()));
            grade.setIdDepartment(String.valueOf(departmentId));
            GradeDAO gradeDAO = new GradeDAO();
            if(gradeDAO.insertClass(grade)){
                JOptionPane.showMessageDialog(tblClass, "Thêm thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblClass, "Thêm thất bại");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            Grade grade = new Grade();
            grade.setIdClass(txtIdClass.getText());
            grade.setClassName(txtClassName.getText());
            String departmentId = dpmDAO.getIdDepartmentByDepartmentName(String.valueOf(cbDepartment.getSelectedItem()));
            grade.setIdDepartment(String.valueOf(departmentId));
            GradeDAO gradeDAO = new GradeDAO();
            if(gradeDAO.updateClass(grade)){
                JOptionPane.showMessageDialog(tblClass, "Cập nhật thành công");
                loadDataToTable();
            }else
                JOptionPane.showMessageDialog(tblClass, "Cập nhật thất bại");
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

