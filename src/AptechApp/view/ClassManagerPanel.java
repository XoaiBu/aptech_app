    private void loadDataToTable(){
        try{
            GradeDAO clsDAO = new GradeDAO();
            listGrade = clsDAO.getAllClass();
            DefaultTableModel tbmSV = (DefaultTableModel) tblClass.getModel();
            tbmSV.setRowCount(0);
            for(Grade grade : listGrade){
                Vector<Object> vtGrade = new Vector<Object>();
                vtGrade.add(grade.getIdClass());
                vtGrade.add(grade.getClassName());
                vtGrade.add(grade.getIdDepartment());
                tbmSV.addRow(vtGrade);
            }
            
            tbmSV.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    private void loadDataToCBDepartment(){
        try{
            DepartmentDAO dpmDAO = new DepartmentDAO();
            ArrayList<String> listDpm = dpmDAO.getDepartmentName();
            String[] array = listDpm.toArray(new String[listDpm.size()]);
            cbDepartment.setModel(new DefaultComboBoxModel<String>(array));
        }catch(Exception e){
            e.printStackTrace();
        }
    }
