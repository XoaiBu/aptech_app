    private void tblRoleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblRoleMouseClicked
       try{
            int row = tblRole.getSelectedRow();
            if(row>=0){
                 String id = (String) tblRole.getValueAt(row, 0);
                RoleDAO rDAO = new RoleDAO();
                Role r = rDAO.findRoleById(id);
                if(r!=null){
                    txtID.setText(id);
                    txtRole.setText(r.getRole());
                }
            }
       }catch(Exception e){
           e.printStackTrace();
       }
       
    }//GEN-LAST:event_tblRoleMouseClicked

