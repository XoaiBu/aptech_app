    private void loadDataToTable(){
        try{
            RoleDAO rDAo = new RoleDAO();
            listRole = rDAo.getAll();
            DefaultTableModel dtmRole = (DefaultTableModel) tblRole.getModel();
            dtmRole.setRowCount(0);
            for(Role r : listRole){
                Vector<Object> vtRole = new Vector<Object>();
                vtRole.add(r.getIdRole());
                vtRole.add(r.getRole());
                dtmRole.addRow(vtRole);
            }
            dtmRole.fireTableDataChanged();
        }catch(Exception e){
            e.printStackTrace();
        }
    }
